            <h1 class="font-bold text-xl md:text-2xl">How does CFD trading work? </h1>
            <p>When you open a contracts for difference (CFD) position you select the number of contracts (the trade size) you would like to buy or sell. Your profit will rise in line with each point the market moves in your favour. </p>
            
