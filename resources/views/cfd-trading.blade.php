            <h1 class="font-bold text-xl md:text-2xl">What are CFDs?</h1>
            <p>A contract for difference (CFD) lets you trade with just a fraction of the value of your trade, which is known as trading on margin, or leveraged trading. This allows traders to open larger positions given their initial capital. Therefore, CFD trading offers greater exposure to global financial markets.</p>
            <p>One of the major benefits of CFD trading is that you can speculate on the asset’s price movements in either direction. You simply buy or sell a contract depending on whether you believe the asset’s price will go up or down. You open a long or a short trade accordingly.</p>
            <p>However, you should always note that leverage trading can amplify your wins, but can also boost your losses.</p>
