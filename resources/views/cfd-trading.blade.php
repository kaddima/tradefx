            <h1 class="font-bold text-xl md:text-2xl">Buy</h1>
            <p>If you think the price of an asset will rise, then you would open a long (Buy) position and profit if the asset price rises in line with your expectations. </p>

            <h1 class="font-bold text-xl md:text-2xl">Sell</h1>
            <p>If you think the price of an asset will fall then you would open a short (Sell) position and profit if it falls in line with your prediction.</p>

            <h1 class="font-bold text-xl md:text-2xl">What is a CFD account?</h1>
            <p>A contract for difference (CFD) account enables you to trade on the price difference of various underlying assets using leverage. Leverage means you put up only a fraction of the amount needed to trade. This is called deposit margin. You will also need to have enough in your account to cover any potential losses if trades go against you. This is called maintenance margin.</p>
            <p>Your broker needs to know a little about you before they can offer you margin trading, so they ask you to set up a special account, proving your identity and ability to cover losses. Often you can practise trading in a demo account, but you will need to add funds to create a CFD trading account before you can trade properly.</p>
            <p>Some regulators require that new customers pass an ‘appropriateness’ test. This often means answering some questions to demonstrate that you understand the increased risks – and not just the potential rewards – of trading on margin. It’s best to thoroughly educate yourself on how leverage and margin work before trading.</p>
            
            <h1 class="font-bold text-xl md:text-2xl">What is leverage in CFD trading? </h1
            <p>When you are trading contracts for difference (CFDs), you hold a leveraged position. This means you only put down a part of the value of your trade and borrow the remainder from your broker. </p>
            <p>Leveraged trading is also referred to as trading on margin. A10% margin means that you have to deposit only 10% of the value of the trade you want to open. The rest is covered by your CFD provider. </p>
            <p>For example, if you want to place an order for $1,000 worth of Brent crude oil and your broker requires 10% of margin, you will need only $100 as the initial amount to open the trade.</p>
            
